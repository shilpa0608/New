package com.learning.java;

public class Arithmatic {
    public static void main(String[] args) {
        int A = 20;
        int B = 40;
        System.out.println(A + B);
        System.out.println(A - B);
        System.out.println(A * B);
        System.out.println(A / B);
        System.out.println(A++);
        System.out.println(A--);

    }}